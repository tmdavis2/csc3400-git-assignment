import math
#this contains main mathematical operations
   
# comment
def add(a, d):
    return a + d

def subtract(num1, num2):
    return num1 - num2 

def multiply(num1, num2):
    return num1 * num2 

def divide(num1, num2):
    return num1 / num2
    
def power(num1, num2):
    return num1 ** num2

def square_root(num1):
    return math.sqrt(num1)

